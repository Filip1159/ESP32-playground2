void Printer::writeInt(int i, Color color) {
	char str[8];
	itoa(i, str, 10);
	writeString(str, color);
}

void Printer::writeHex(int i, Color color) {
	char str[8];
	itoa(i, str, 16);
	writeString(str, color);
}

void Printer::writeString(const char* text, Color color) {
	while (*text)
		writeChar(*text++, color);
}

void Printer::writeChar(char ch, Color color) {
    if (fontSize == Small)
        renderSmallChar(ch, color);
    else renderBigChar(ch, color);
}

void Printer::renderSmallChar(char ch, Color color) {
	uint8_t mask = 0x01;
	display->setAddrWindow(cursorX*CHAR_WIDTH, cursorY*CHAR_HEIGHT, cursorX*CHAR_WIDTH+4, cursorY*CHAR_HEIGHT+6);
	display->sendCommand(RamWrite);
	for (uint8_t row = 0; row < CHAR_HEIGHT - 1; row++) {
		for (uint8_t col = 0; col < CHAR_WIDTH - 1; col++) {
			if ((FONT_CHARS[ch-' '][col] & mask) == 0) display->sendData16Bit((uint16_t)Black);
			else display->sendData16Bit((uint16_t)color);
		}
		mask <<= 1;
	}
	advanceCursor();
}

void Printer::renderBigChar(char ch, Color color) {
    uint8_t mask = 0x01;
	display->setAddrWindow(cursorX*10, cursorY*16, cursorX*10+9, cursorY*16+15);
	display->sendCommand(RamWrite);
	for (uint8_t row = 0; row < 8; row++) {
		for (uint8_t col = 1; col < 20; col+=2) {
			if ((BIG_FONT[(uint8_t)ch][col] & mask) == 0) display->sendData16Bit((uint16_t)Black);
			else display->sendData16Bit((uint16_t)color);
		}
		mask <<= 1;
	}
    mask = 0x01;
    for (uint8_t row = 0; row < 8; row++) {
		for (uint8_t col = 0; col < 20; col+=2) {
			if ((BIG_FONT[(uint8_t)ch][col] & mask) == 0) display->sendData16Bit((uint16_t)Black);
			else display->sendData16Bit((uint16_t)color);
		}
		mask <<= 1;
	}
	advanceCursor();
}

void Printer::setFontSize(FontSize fontSize) {
    this->fontSize = fontSize;
}

void Printer::setCursor(uint8_t x, uint8_t y) {
    if ((mode == Portrait && (x > PORTRAIT_MAX_CURSOR_X || y > PORTRAIT_MAX_CURSOR_Y)) || 
        (mode == Horizontal && (x > HORIZONTAL_MAX_CURSOR_X || y > HORIZONTAL_MAX_CURSOR_Y))) {
        throw new invalid_argument("Invalid cursor coordinates: (" + $(x) + ", " + $(y) + ")");
    }
	cursorX = x;
	cursorY = y;
}
void Printer::setLine(uint8_t y) {
    if ((mode == Portrait && y > PORTRAIT_MAX_CURSOR_Y) || 
        (mode == Horizontal && y > HORIZONTAL_MAX_CURSOR_Y)) {
        throw new invalid_argument("Invalid cursor coordinates: (" + $(0) + ", " + $(y) + ")");
    }
	cursorX = 0;
	cursorY = y;
}

void Printer::advanceCursor() {
	cursorX++;
	if ((mode == Portrait && cursorX > PORTRAIT_MAX_CURSOR_X) || (mode == Horizontal && cursorX > HORIZONTAL_MAX_CURSOR_X)) {
		cursorY++;
		cursorX = 0;
	}
	if ((mode == Portrait && cursorY > PORTRAIT_MAX_CURSOR_Y) || (mode == Horizontal && cursorY > HORIZONTAL_MAX_CURSOR_Y))
		cursorY = 0;
}